  /**
   * Construction.
   * @param t Overal Stop time
   * @param m number of cycles
   * @param tm stability limit
   * @param reorder reording flag
   */
  public FedStep (float t, int m, float tm) {
    _t = t;
    _m = m;
    _tm = tm;
  }

  /**
   * Comput step sizes in each cycle.
   * @param nc number of carray of step sizes to be computed.
   */
  public float[] getSteps(boolean reorder) {
    float tc = _t/_m; //stop time for each cycle
    int nc = (int)(ceil(sqrt(3f*tc/_tm + 0.25f) - 0.5f - 1.0e-8f)+ 0.5f);
    float scale = 3f*tc/(_tm*(float)(nc*(nc+1)));
    float[] tau = new float[nc];
    fed_tau_internal(nc,scale,_tm,reorder,tau);
    return tau;
  }

