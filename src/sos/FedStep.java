  /**
   * Compute number of cycles and step sizes for all steps.
   * @param t number of steps for each cycle.
   * @param scale Ratio of t we search to maximal t.
   * @param tm stability limit
   * @param reorder reording flag
   * @param tau array of step sizes to be computed.
   */
  private void fed_tau_internal( 
    int n, float scale, float tm, boolean reorder, float[] tau)
  {
    float[] tauh = new float[n]; // Helper array for unsorted taus
    if(n <= 0) return;
    // Compute time saver
    float c = 1.0f/(4.0f*(float)n + 2.0f);
    float d = scale*tm/2.0f;

    // Set up originally ordered tau vector
    for (int k = 0; k < n; ++k) {
      float h = cos((float)Math.PI * (2.0f * (float)k + 1.0f) * c);
      if (reorder) tauh[k] = d/(h*h);
      else tau[k] = d/(h*h);
    }

    int kappa;
    if (reorder) {
      if (n>FED_MAXKAPPA) kappa = n / 4;
      else kappa = fed_kappalookup[n];

      // Get modulus for permutation   
      int prime = n + 1;
      while (!fed_is_prime_internal(prime))
        prime++;

      //Perform permutation
      for (int k=0, l=0; l<n; ++k, ++l) {
        int index;
        while ((index = ((k+1)*kappa) % prime - 1) >= n)
          k++;
        tau[l] = tauh[index];
      }
    }
  }
  private boolean  fed_is_prime_internal(int number) {
    int s = (int)sqrt((float)number + 1.0f);
    for (int i=2; i<=s; ++i)
      if (number%i==0) return false;
    return true;
  }


